public class Maximum_Tree_Num {
    public static void main(String[] args) {
        int a = 67;
        int b = 120;
        int c = 45;
        if (a >= b && a >= c) {
            System.out.println("A Is Grater");
        }
        if (b >= a && b >= c) {
            System.out.println("B Is Grater");
        }
        if (c >= a && c >= b) {
            System.out.println("C is Grater");
        }
    }
}
